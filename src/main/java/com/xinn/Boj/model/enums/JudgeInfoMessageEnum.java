package com.xinn.Boj.model.enums;

import org.apache.commons.lang3.ObjectUtils;

import java.util.Arrays;
import java.util.List;
import java.util.stream.Collectors;

/**
 * 用户角色枚举
 *
 * @author Xinn Li
 *  
 */
public enum JudgeInfoMessageEnum {
    AC("成功","accepted"),
    WA("答案错误","Wrong Answer"),
    CE("编译错误","Compile Error"),
    TLE("运行超时","Time Limit Exceeded"),
    RE("运行错误","Runtime Error"),
    MLE("内存超限","Memory Limit Exceeded"),
    PE("格式错误","Presentation Error"),
    OLE("输出超限","Output Limit Exceeded");


    private final String text;

    private final String value;

    JudgeInfoMessageEnum(String text, String value) {
        this.text = text;
        this.value = value;
    }

    /**
     * 获取值列表
     *
     * @return
     */
    public static List<String> getValues() {
        return Arrays.stream(values()).map(item -> item.value).collect(Collectors.toList());
    }

    /**
     * 根据 value 获取枚举
     *
     * @param value
     * @return
     */
    public static JudgeInfoMessageEnum getEnumByValue(String value) {
        if (ObjectUtils.isEmpty(value)) {
            return null;
        }
        for (JudgeInfoMessageEnum anEnum : JudgeInfoMessageEnum.values()) {
            if (anEnum.value.equals(value)) {
                return anEnum;
            }
        }
        return null;
    }

    public String getValue() {
        return value;
    }

    public String getText() {
        return text;
    }
}
